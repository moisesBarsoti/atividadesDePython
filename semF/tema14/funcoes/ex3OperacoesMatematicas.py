# Soma
def soma(n1, n2):
    return n1 + n2
# Subtração
def subtracao(n1, n2):
    return n1 - n2
# Multiplicação
def multiplicacao(n1, n2):
    return n1 * n2
# Divisão
def divisao(n1, n2):
    return n1 / n2

# Instaciando
contaDeSoma = soma(10,10)
contaDeSubtracao = subtracao(10,10)
contaDeMultiplicacao = multiplicacao(10,10)
contaDeDivisao = divisao(10,10)

# Imprimindo
print("")
print("Resultados: ")
print("")
print("Soma:", contaDeSoma)
print("Subtração:", contaDeSubtracao)
print("Multiplicação:", contaDeMultiplicacao)
print("Divisão:", contaDeDivisao)