# Lista
numerosFloat = [10.5, 12.5, 0.2, 8.5]
print(f"Lista: {numerosFloat}")

# Menor elemento da lista e o indice
menorNumeroFloat = min(numerosFloat)
menorNumeroIndice = numerosFloat.index(menorNumeroFloat)

# Imprimindo valores
print("")
print(f"O menor valor e: {menorNumeroFloat}")
print(f"O indice do menor valor e: {menorNumeroIndice}")